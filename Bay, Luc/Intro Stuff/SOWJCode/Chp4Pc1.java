package SOWJCode;

public class Chp4Pc1 
{
	public static void main(String[] args)
	{
		 int score = 9;
     
    		 if(1 <= score && score <= 10)
	 	{
			if(score == 1)
				System.out.print("I \n");
			else if(score == 2)
				System.out.print("II \n");
			else if(score == 3)
				System.out.print("III \n");
			else if(score == 4)
				System.out.print("IV \n");
			else if(score == 5)
				System.out.print("V \n");
			else if(score == 6)
				System.out.print("VI \n");
			else if(score == 7)
				System.out.print("VII \n");
			else if(score == 8)
				System.out.print("VIII \n");
			else if(score == 9)
				System.out.print("IX \n");
			else
				System.out.print("X \n");
		}
		
		else
			System.out.print("Not a valid number \n");
		 
	 }
       		           
}
