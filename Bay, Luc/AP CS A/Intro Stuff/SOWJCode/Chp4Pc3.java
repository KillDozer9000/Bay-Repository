package SOWJCode;

public class Chp4Pc3 
{
	public static void main(String[] args)
	{
		 int score = 90;
     
    	 	if(90 <= score && score <= 100)
       		 System.out.print("Grade is an A");  
     	 else if(80 <= score && score <= 89)
        	 System.out.print("Grade is a B");   
     	 else if(70 <= score && score <= 79)
        	 System.out.print("Grade is a C");         
     	 else if(60 <= score && score <= 69)
        	 System.out.print("Grade is a D");
     	 else
        	 System.out.print("Grade is a F");         
	}
}
